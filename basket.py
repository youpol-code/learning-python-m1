print(f"ЁЯСЙ р╕Кр╕┤р╣Йр╕Щр╕Чр╣Йр╕▓р╕в: {last_item}")

# --- 3. Update (р╣Ар╕Ыр╕ер╕╡р╣Ир╕вр╕Щр╕Вр╕нр╕З) ---
# р╣Ар╕Ыр╕ер╕╡р╣Ир╕вр╕Щр╣Гр╕Ир╣Др╕бр╣Ир╣Ар╕нр╕▓ Adapter р╕Ир╕░р╣Ар╕нр╕▓ Trackpad р╣Бр╕Чр╕Щ
