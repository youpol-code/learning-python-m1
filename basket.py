# --- 4. Add (р╣Ар╕Юр╕┤р╣Ир╕бр╕Вр╕нр╕З) ---
# append() = р╕Хр╣Ир╕нр╕Чр╣Йр╕▓р╕вр╣Бр╕Цр╕з (р╕ер╕╣р╕Бр╕Др╣Йр╕▓р╕лр╕вр╕┤р╕Ър╕Вр╕нр╕Зр╣Ар╕Юр╕┤р╣Ир╕б)
