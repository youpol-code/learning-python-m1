# --- 1. Create (р╕кр╕гр╣Йр╕▓р╕Зр╕Хр╕░р╕Бр╕гр╣Йр╕▓) ---
# Type Hint: list[str] р╕Ър╕нр╕Бр╕зр╣Ир╕▓р╣Гр╕Щр╕Щр╕╡р╣Йр╣Ар╕Бр╣Зр╕Ър╣Др╕Фр╣Йр╣Бр╕Др╣И "р╕Вр╣Йр╕нр╕Др╕зр╕▓р╕б"

