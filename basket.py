# 3. р╕Бр╕▓р╕гр╣Ар╕гр╕╡р╕вр╕Зр╕ер╕│р╕Фр╕▒р╕Ър╕Кр╕▒р╣Ир╕зр╕Др╕гр╕▓р╕з (Temporary Sort)
